let a = 5;
let b = 10;

// Addition
let sum = a + b; // 15

// Subtraction
let difference = b - a; // 5

// Multiplication
let product = a * b; // 50

// Division
let quotient = b / a; // 2

// Modulus
let remainder = b % a; // 0

console.log(`Sum: ${sum}`);
console.log(`Difference: ${difference}`);
console.log(`Product: ${product}`);
console.log(`Quotient: ${quotient}`);
console.log(`Remainder: ${remainder}`);