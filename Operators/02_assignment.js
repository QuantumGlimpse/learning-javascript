let a = 10;
a += 5; // a = a + 5
console.log(`Value of a after addition: ${a}`);
a -= 3; // a = a - 3
console.log(`Value of a after subtraction: ${a}`);
a *= 2; // a = a * 2
console.log(`Value of a after multiplication: ${a}`);
a /= 4; // a = a / 4
console.log(`Value of a after division: ${a}`);
a %= 3; // a = a % 3

console.log(`Final value of a: ${a}`);